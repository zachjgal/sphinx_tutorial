def quick_mafs(x=2, y=2):
    """
    .. math::
       2 + 2 is 4
      
    .. math::
       -1 that's 3
    
    Args:
        x (float): The first number.
        y (float): The second number.
        
    Returns:
       float: One less than the sum of the two numbers.
    """
    return x + y - 1




